// naive downsample
void downsampleFont(const uint8_t *font, size_t s, int16_t w, int16_t h,
                    iscale_t scale) {

    int16_t bpl = (w + 7) / 8; // Bitmap scanline pad whole "bytes per line"
    int byte = 0;

    int fbits[w][h];

    // expand font to "bitmap" pixels (on/off)
    for (int16_t j = 0; j < h; j++) {
        for (int16_t i = 0; i < w; i++) {
            if (i & 7)
                byte <<= 1;
            else
                byte = font[j * bpl + i / 8];
            fbits[i][j] = (byte & 0x80) ? 1 : 0;
        }
    }

    // downscale in x only - we'll want to fix this to <= 18px
    float fct = 0.80;

    int dw = (int)(w * fct);
    if (dw > 18)
        dw = 18; // pin
    int dh = h;  // pin - (int)(h * fct);

    bool ok = false;
    int idx = 0;
    int agg = 0;

    bpl = (dw + 7) / 8;
    double xscale = (dw + 0.0) / w;
    double yscale = (dh + 0.0) / h;
    double threshold = 0.5 / (xscale * yscale);
    double yend = 0.0;

    clockF = (uint8_t *)malloc(bpl * dh * sizeof(uint8_t));
    uint16_t retf1c = 0;

    for (int f = 0; f < dh; f++) { // y on output

        byte = 7;
        agg = 0;
        ok = false;

        double ystart = yend;
        yend = (f + 1) / yscale;
        if (yend >= h)
            yend = h - 0.000001;
        double xend = 0.0;
        for (int g = 0; g < dw; g++) { // x on output
            double xstart = xend;
            xend = (g + 1) / xscale;
            if (xend >= w)
                xend = w - 0.000001;
            double sum = 0.0;
            for (int y = (int)ystart; y <= (int)yend; ++y) {
                double yportion = 1.0;
                if (y == (int)ystart)
                    yportion -= ystart - y;
                if (y == (int)yend)
                    yportion -= y + 1 - yend;
                for (int x = (int)xstart; x <= (int)xend; ++x) {
                    double xportion = 1.0;
                    if (x == (int)xstart)
                        xportion -= xstart - x;
                    if (x == (int)xend)
                        xportion -= x + 1 - xend;
                    sum += fbits[x][y] * yportion *
                           xportion; // can use "raw" bit here ???
                }
            }

            ok = true;
            if (sum > threshold) {
                agg += pow(2, byte);
            }

            byte--;

            if (idx != 0 && (0 == ((idx + 1) % (dw)))) {
                byte = -1;
            }

            // roll over
            if (byte < 0) {
                byte = 7;
                clockF[retf1c] = agg;
                retf1c++;
                agg = 0;
                ok = false;
            }

            idx++;
        }

        // in flight - snag 'em
        if (ok) {
            clockF[retf1c] = agg;
            retf1c++;
            ok = false; // safe
        }
    }

    free(fbits);
}

